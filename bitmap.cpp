bitmap::bitmap():effect("Bitmap",1) {
  speed = newintparam("speed", -50, 50, 20);
  picture = newstringparam("pic", "smiley");
  
  usemap(smiley);
}

void bitmap::usemap(const uint8_t *data) {
  memcpy_P(field->r, data, 60*19);
  memcpy_P(field->g, data+60*19, 60*19);
  memcpy_P(field->b, data+2*60*19, 60*19);
  //memcpy(field->r, data, 60*19);
  //memcpy(field->g, data+60*19, 60*19);
  //memcpy(field->b, data+2*60*19, 60*19);
}

void bitmap::usemap(char *s) {
  if (!s) {
    usemap(smiley);
    return;
  }
  if (strcasecmp(s,"earth") == 0) 
    usemap(earth);
  else if (strcasecmp(s,"smiley") == 0) 
    usemap(smiley);
  else if (strcasecmp(s,"kuerbis") == 0) 
    usemap(kuerbis);
  else
    usemap(smiley);
}

void bitmap::updateparams() {
  updateparam(picture);  
}

void bitmap::updateparam(param_t *param) {
  if (param == picture) {
    usemap(picture->d.s.val);
  }
}

void bitmap::tick() {
  if (speed->d.i.val == 0) return;
  int dir = (speed->d.i.val < 0)?-1:1;
  int s = (speed->d.i.val < 0)?-speed->d.i.val:speed->d.i.val;
  if (s < 20) {
    // 0..19
    int spd = 21 - s; // 2..21
    if (flowctr >= spd) {
      flowctr = 0;
      rollfield(dir);
    }
    flowctr++;
  } else {
    // 20..
    int spd = s - 19; // 1..x
    rollfield(dir * spd);
  }
}
