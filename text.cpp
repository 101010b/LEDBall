void text::tick() {
  uint8_t r, g, b;
  if (useglobalcol->d.b.val && globalcol) {
    r = globalcol->r;
    g = globalcol->g;
    b = globalcol->b;
  } else {
    r = col->d.c.r;
    g = col->d.c.g;
    b = col->d.c.b;
  }

  for (int spc=0;spc < speed->d.i.val;spc++) {
    // Shift left
    shiftfield(-1);
    if ((curchar > 32) && (curcol < (curcharw*2))) {
      // Not blank space
      uint32_t ofs = curcharofs + (curcol/2)*13; 
      for (int i=0;i<13;i++) {
        uint8_t d = pgm_read_byte(fontdata+ofs);
        ofs++;
        uint16_t data = d;
        if (data > 0) {
          field->r[(16-i)*LEDW-1]=data * r / 256;
          field->g[(16-i)*LEDW-1]=data * g / 256;
          field->b[(16-i)*LEDW-1]=data * b / 256;
        }
      }
    }  
    curcol++;
    if (curcol == ((curcharw + 1)*2)) {
      // Next char
      char c = 'A';
      if (!message->d.s.val) {
        charpos = -1;
        c = ' ';
      } else {
        charpos++;
        if (charpos < strlen(message->d.s.val))
          c = message->d.s.val[charpos];
        else if (charpos < strlen(message->d.s.val) + 4)
          c = ' ';
        else {
          charpos = 0;
          c = message->d.s.val[charpos];
        }
      }
      nextchar(c);
    }
  }
}
