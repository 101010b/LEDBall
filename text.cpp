void text::nextchar(uint8_t c) {
  if (c < 32) c = 32;
  if (c > 127) c = 127;
  curchar = c;
  if (c == 32) {
    curcharw = 4;
    curcharofs = 0;
  } else {
    uint32_t fonti = fontinfo[curchar-32];
    curcharw = fonti & 0x0000FFFF;
    curcharofs = ((fonti & 0xFFFF0000) >> 16);
  }
  curcol = 0;
}

